/**
 * 0101 – Operaciones básicas.
 * Muestra la suma, resta, multiplicación y división de dos enteros
 * largos prefijados, así como el resto de dividir el primero entre el segundo.
 */

fun main() {
    val n1: Int = 23
    val n2: Int = 11

    println("n1 = $n1")
    println("n2 = $n2")
    println("n1+n2 = ${n1 + n2}")
    println("n1-n2 = ${n1 - n2}")
    println("n1*n2 = ${n1 * n2}")
    println("n1/n2 = ${n1 / n2}")
    println("n1%n2 = ${n1 % n2}")
}